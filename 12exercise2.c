#include <stdio.h> // 표준 입출력 라이브러리 사용

int main() { // 메인 함수 시작
    char A[201]; // 입력받은 문자열을 저장할 배열
    char B[101]; // 찾을 단어를 저장할 배열
    char word[101]; // 입력받은 문자열에서 추출한 저장할 배열
    int i = 0, count = 0; // 반복문에서 사용할 인덱스 i, B와 일치하는 단어의 개수

    gets(A); // 문자열 A 입력 받기
    gets(B); // 찾을 단어 B 입력 받기

    while (1) // 무한 반복 (문자열 끝에서 break)
    { 
        int w = 0; // word의 인덱스 w

        while (A[i] != ' ' && A[i] != '.' && A[i] != '\0') // 단어 하나 추출 (공백, 마침표, 문자열 끝 전까지)
        {
            word[w] = A[i]; // 단어 배열에 문자 저장
            w = w + 1;      // 단어 배열 인덱스 증가
            i = i + 1;      // 문자열 A 인덱스 증가
        }
        word[w] = '\0'; // 단어 끝에 널 문자 추가

        // word와 B가 정확히 일치하는지 확인 (직접 비교)
        int j = 0; // 비교용 인덱스
        while (word[j] == B[j] && word[j] != '\0' && B[j] != '\0') // 두 단어가 같으면
        {
            j++; // 다음 글자 비교
        }
        if (word[j] == '\0' && B[j] == '\0') // 두 단어가 모두 끝났으면
        {
            count++; // 일치 개수 증가
        }

        if (A[i] == ' ' || A[i] == '.') // 공백 또는 마침표를 만나면
        {
            i++; // 다음 단어로 넘어가기 
        }

        if (A[i] == '\0') break; // 문자열 끝이면 반복 종료
    }

    printf("%d\n", count); // 일치하는 단어 개수 출력

    return 0; // 프로그램 종료
}